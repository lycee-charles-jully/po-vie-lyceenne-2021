    <h1>Atelier confiance en soi et réussite</h1>

    <p>
        L’atelier « Confiance en Soi et Réussite » a été créé cette année pour les élèves qui préparent les épreuves du
        baccalauréat.
    </p>
    <p>
        Les examens suscitent souvent beaucoup d’appréhension, d’angoisses, de stress, des difficultés de concentration,
        de gestion des émotions, des problèmes de sommeil et surtout un manque de confiance en soi.
    </p>
    <p class="space-bottom">
        Cet atelier propose des séances de méditation de pleine conscience avec Nathalie, professeur de physique ainsi
        que des séances de sophrologie avec Valérie, sophrologue.
    </p>

    <p>
        Durant les séances de méditation de pleine conscience, les élèves sont amenés à fixer leur attention sur
        l’instant présent et à observer les sensations ou pensées qui se présentent à l’esprit, avec bienveillance et
        sans jugement.
    </p>

    <p>
        Complémentaires, les séances de sophrologie qui suivent, proposent aux élèves des exercices de mouvements doux,
        basés sur la respiration contrôlée, la détente musculaire et la visualisation d’images positives.
    </p>

    <p>
        Ces deux disciplines associées amèneront progressivement les élèves à retrouver un équilibre et un apaisement
        nécessaires pour aborder les épreuves d’examen avec confiance et sérénité.
    </p>
    <p>
        Toutefois, une présence assidue à toutes les séances est recommandée pour une efficacité optimale.
    </p>
